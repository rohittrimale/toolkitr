export function decodeAddr(hi: number, lo: number): number {
  if ((hi & 0xC0) === 0x00) return ((hi & 0x3F) << 8) | (lo & 0xFF)
  return ((hi & 0x3F) << 6) | (lo & 0x3F)
}
export function encodeAddr(addr: number): [number, number] {
  return [BA_ENC[(addr >> 6) & 0x3F], BA_ENC[addr & 0x3F]]
}

// 3270 color code → CSS
export function colorToCss(code: number): string | null {
  switch (code) {
    case 0xF1: return '#4d9fff'   // blue
    case 0xF2: return '#ff6b6b'   // red
    case 0xF3: return '#ff9fff'   // pink
    case 0xF4: return '#00cc44'   // green
    case 0xF5: return '#00cccc'   // turquoise
    case 0xF6: return '#ffee00'   // yellow
    case 0xF7: return '#e0e0e0'   // white
    default:   return null
  }
}
